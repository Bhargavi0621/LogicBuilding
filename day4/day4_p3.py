"""Print a Number in Reverse Order:
Write a program where the user enters a number, and the program prints it in reverse order. For example:
Input: 1234
Output: 4321"""
number=(input("Enter a number:"))
reverse=number[::-1]
print("Reverse of the number is:",reverse)
